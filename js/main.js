/*Metodos Math.min
    retorna el menor de cero o más números dados como parámetros de entrada
*/

console.log("Calcular el numero menor de una serie de numeros")

console.log("Numero menor de: 1, 3, 2  =  ",Math.min(1, 3, 2));
console.log("Numero menor de: -1, -3, -2  =  ",Math.min(-1, -3, -2));

const array1 = [1, 3, 2];
console.log("Numero menor de ",array1)
console.log(Math.min(...array1));


  

